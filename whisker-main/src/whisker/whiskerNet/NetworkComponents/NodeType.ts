/**
 * A enum that shows all available types of Network Nodes
 */
export enum NodeType {
    /**
     * Value for an Input Node
     */
    INPUT,

    /**
     * Value for a Bias Node
     */
    BIAS,

    /**
     * Value for a Hidden Node
     */
    HIDDEN,

    /**
     * Value for an output node
     */
    OUTPUT
}
